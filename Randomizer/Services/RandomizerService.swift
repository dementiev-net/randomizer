import Foundation

// MARK: - Randomizer Service Protocol

/// Протокол сервиса генерации случайных чисел и расчёта рейтинга
///
/// Определяет интерфейс для генерации случайных значений и
/// вычисления соответствующего рейтинга на основе сгенерированного числа.
protocol RandomizerServiceProtocol {
    
    /// Генерирует случайное число
    ///
    /// - Returns: Случайное целое число
    func generateNumber() -> Int
    
    /// Вычисляет рейтинг на основе числа
    ///
    /// - Parameter number: Число для оценки
    /// - Returns: Рейтинг от 0 до 5 звёзд
    func calculateRating(for number: Int) -> Int
}

// MARK: - Randomizer Service

/// Сервис генерации случайных чисел и расчёта рейтинга
///
/// Генерирует случайные числа в диапазоне 1-99 и присваивает им рейтинг
/// от 0 до 5 звёзд в зависимости от величины числа.
///
/// ## Шкала рейтинга:
/// - 5 звёзд: 75-99
/// - 4 звезды: 66-74
/// - 3 звезды: 50-65
/// - 2 звезды: 33-49
/// - 1 звезда: 25-32
/// - 0 звёзд: 1-24
class RandomizerService: RandomizerServiceProtocol {
    
    /// Генерирует случайное число в диапазоне 1-99
    ///
    /// - Returns: Случайное целое число от 1 до 99 (включительно)
    func generateNumber() -> Int {
        return Int.random(in: 1..<100)
    }
    
    /// Вычисляет рейтинг (количество звёзд) для заданного числа
    ///
    /// - Parameter number: Число для оценки (обычно от 1 до 99)
    /// - Returns: Рейтинг от 0 до 5 звёзд
    ///
    /// Логика оценки:
    /// - **5 звёзд**: 75 и выше (отлично)
    /// - **4 звезды**: 66-74 (хорошо)
    /// - **3 звезды**: 50-65 (нормально)
    /// - **2 звезды**: 33-49 (ниже среднего)
    /// - **1 звезда**: 25-32 (плохо)
    /// - **0 звёзд**: меньше 25 (очень плохо)
    func calculateRating(for number: Int) -> Int {
        switch number {
        case 75...: return 5
        case 66...: return 4
        case 50...: return 3
        case 33...: return 2
        case 25...: return 1
        default:    return 0
        }
    }
}
